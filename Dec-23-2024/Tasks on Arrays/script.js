// Tasks on Arrays

// 1. Create an array with at least 5 numbers
let numbers = [3, 7, 2, 9, 5];
console.log("Numbers:", numbers);

// Add a number to the end
numbers.push(12);
console.log("After adding a number:", numbers);

// Remove the first number
numbers.shift();
console.log("After removing the first number:", numbers);

// 2. Create an array of strings
let strings = ["apple", "orange", "banana", "grape", "pear"];
console.log("Strings:", strings);
